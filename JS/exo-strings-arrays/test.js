
console.log(FUNCTION)
function add(nb1, nb2) {
  return nb1 + nb2
}

// Appelle de la fonction add avec les 2 arguments 1 et 2
let sum = add(1, 2)
console.log(sum)

console.log(CONST)
const add = (nb1, nb2) => {
  return nb1 + nb2
}

// Appelle de la fonction add avec les 2 arguments 1 et 2
let sum = add(1, 2)
console.log(sum)
